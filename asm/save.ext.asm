	mov	r0,0x0
	strb	r0,[r1,0xC]	// year
	mov	r0,0x1
	strb	r0,[r1,0xD]	// month
	strb	r0,[r1,0xE]	// day
	mov	r0,0x0
	strb	r0,[r1,0xF]	// hour
	strb	r0,[r1,0x10]	// minute
	mov	r0,0x6
	strb	r0,[r1,0x11]	// weekday

