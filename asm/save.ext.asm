@chipNameToID:
// in r0 = chip name ptr
// out r0 = chip ID (0 = none)
	push	r4-r5,r14
	mov	r5,r0

	// Read first char
	ldrb	r0,[r5]
	cmp	r0,0x9F
	bgt	@@notfound

	// Get pointer to first string to check
	add	r1,=@@chipNameTable
	lsl	r0,r0,0x1
	ldrh	r0,[r1,r0]
	tst	r0,r0
	beq	@@notfound
	add	r1,r1,r0

@@checkString:
	ldrb	r0,[r1]
	ldrb	r2,[r1,0x1]
	lsl	r2,r2,0x8
	orr	r0,r2
	beq	@@notfound

	add	r1,0x3
	add	r2,r5,0x1

@@checkChar:
	ldrb	r3,[r1]
	ldrb	r4,[r2]
	add	r1,0x1
	add	r2,0x1
	cmp	r3,r4
	bne	@@nextString
	cmp	r3,0xE5
	beq	@@end
	b	@@checkChar

@@nextString:
	cmp	r3,0xE5
	beq	@@checkString
	ldrb	r3,[r1]
	add	r1,0x1
	b	@@nextString

@@notfound:
	mov	r0,0x0

@@end:
	pop	r4-r5,r15

	.pool

